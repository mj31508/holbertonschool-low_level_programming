#include "_3_CALC_H"

/**
 * op_add - function to add to integers
 * @a: first variable for addition
 * @b: second variable for addition
 * Return: int
 **/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtraction function
 * @a: first variable for subtraction
 * @b: second number for subtracton
 * Return: int
 **/
int op_sub(int a, int b)
{
	return (a - b);

}

/**
 * op_mul - Funtion that muliplies two variables
 * @a: First variable to be multiplied
 * @b: Second variable to be multiplied
 * Return: int
 **/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function that divides two intergers
 * @a: First int to be divided
 * @b: Second int to be divided
 * Return: int
 **/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function that divides two integers and returns remainder
 * @a: First integer to be divided
 * @b: Second integer to be divided
 * Return: int
 **/
int op_mod(int a, int b)
{
	return (a % b);
}
